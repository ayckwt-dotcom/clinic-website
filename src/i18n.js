/** Build a translator for the current lang. Falls back to the key path if missing. */
export const makeT = (lang) => (path) =>
  path.split(".").reduce((obj, key) => (obj && obj[key] != null ? obj[key] : undefined), dict[lang]) ?? path;

/** (Optional) central place to set HTML attrs */
export const setLangAttrs = (lang) => {
  document.documentElement.setAttribute("lang", lang);
  document.documentElement.setAttribute("dir", lang === "ar" ? "rtl" : "ltr");
};
